 //ONE
 const employeeOne = document.querySelector('.employee-one');
 const modOne = document.querySelector('.mod-one');
 const buttonOne = document.getElementById('button-one');

 employeeOne.addEventListener( 'click', (e) => {
    if (employeeOne === e.target) {
        modOne.style.display = 'grid';
    } 
 });

modOne.addEventListener( 'click', e => {
    if (buttonOne === e.target) {
        modOne.style.display = 'none';
    }
});

